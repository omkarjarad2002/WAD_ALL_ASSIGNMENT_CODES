const mongoose = require("mongoose");

const userSchama = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const User = mongoose.model("USERS", userSchama);
module.exports = User;
